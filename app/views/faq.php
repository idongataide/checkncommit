                                    <div class="panel-body">
                                    If you're unfamiliar with a vendor/organization, it's a good idea to check their website and other internet sources for warning signs. For example:
                                          <br><br>      <b>     •	Is the language strange? </b> Websites of real and professional businesses are usually well put together. If the text is littered with mistakes and the website looks unorganized, it could be a sign of a scam.
                                          <br><br>      <b>     •	Does the website’s address correspond to the type of product being sold?</b> Fraudsters can buy up website addresses that used to belong to legitimate web stores that have gone out of business, and then use these domains to create fake shops. If the website’s name or address is strange, or not in accordance with the products being offered for sale, then this could indicate a fake online shop.
                                          <br><br>      <b>    •	Are any contact details provided for the business?</b> If so, do these fit with the other aspects of the site, and when put into a search engine come back as being associated with the company name? Genuine web stores shouldn’t have anything to hide, but fake websites often have no contact details available, or list illegitimate phone numbers or addresses.
                                          <br><br>      <b>    •	Other online research.</b> Do a Google search and see if anyone else has written about the website? If the results don’t add up, or there is little information available, proceed with caution.
                                          <br><br>     <b>    •	Use common sense and trust your instincts.</b> Have you come across a deal that seems too good to be true? If so, chances are, it probably is.

